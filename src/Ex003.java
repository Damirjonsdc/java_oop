public class Ex003 {
    // Абстрактный класс Товар
    abstract class Товар {
        protected String название;
        protected double цена;

        public String getНазвание() {
            return название;
        }

        public double getЦена() {
            return цена;
        }

        public abstract void продать();
    }

    // Класс БутылкаВоды, наследуется от класса Товар
    class БутылкаВоды extends Товар {
        public БутылкаВоды(String название, double цена) {
            this.название = название;
            this.цена = цена;
        }

        @Override
        public void продать() {
            System.out.println("Продана бутылка воды " + название + " за " + цена + " рублей");
        }
    }

    // Интерфейс ТорговыйАвтомат
    interface ТорговыйАвтомат {
        void добавитьТовар(Товар товар);
        void удалитьТовар(Товар товар);
        void продатьТовар(Товар товар);
    }

    // Реализация класса ТорговогоАвтомата
    class ПродающийБутылкиВодыАвтомат implements ТорговыйАвтомат {
        private List<Товар> товары;

        public ПродающийБутылкиВодыАвтомат() {
            this.товары = new ArrayList<>();
        }

        @Override
        public void добавитьТовар(Товар товар) {
            товары.add(товар);
        }

        @Override
        public void удалитьТовар(Товар товар) {
            товары.remove(товар);
        }

        @Override
        public void продатьТовар(Товар товар) {
            товар.продать();
            товары.remove(товар);
        }

        public void показатьНаличие() {
            System.out.println("На данный момент имеется в наличии:");
            for (Товар товар : товары) {
                System.out.println(товар.getНазвание() + " - " + товар.getЦена() + " рублей");
            }
        }
    }

    public class Main {
        public static void main(String[] args) {
            ПродающийБутылкиВодыАвтомат автомат = new ПродающийБутылкиВодыАвтомат();
            БутылкаВоды бутылка1 = new БутылкаВоды("Aqua", 50.0);
            БутылкаВоды бутылка2 = new БутылкаВоды("BonAqua", 45.0);

            автомат.добавитьТовар(бутылка1);
            автомат.добавитьТовар(бутылка2);
            автомат.показатьНаличие();

            автомат.продатьТовар(бутылка1);
            автомат.показатьНаличие();
        }
    }
}
